#include <stdlib.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int	i;

	i = 0;
	if (!s1 || !s2)
		return (0);
	if (n == 0)
		return (1);
	while (s1[i] && s2[i] && s1[i] == s2[i] && (size_t)i < n - 1)
		i++;
	return (s1[i] == s2[i] ? 1 : 0);
}
